an open-source wireless network scanner that collects GPS data for the Mozilla Location Service,

our crowd-sourced location database. As you move around, the app "stumbles" upon new Wi-Fi

networks and cell towers. The Mozilla Location Service combines these wireless network

measurements to provide geolocation services for Firefox OS devices and other open source

projects.
